def get_map(input):
  maplines = input.split('\n')
  height = len(maplines)
  width = len(maplines[0])
  trees=0
  j=0
  for i in range(1,height):
    j+=3
    j = j % width
    if maplines[i][j] == '#':
      trees+=1
  print(trees)
      
def get_map2(input, right, down):
  maplines = input.split('\n')
  height = len(maplines)
  width = len(maplines[0])
  trees=0
  j=0
  for i in range(down,height):
    if i % down == 0:
	    j+=right
	    j = j % width
	    if maplines[i][j] == '#':
	      trees+=1
  print(trees)
  return trees

